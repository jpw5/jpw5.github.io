let randomSelection = "";
let newPassword = "";

function securePassword() {
  let password = document.getElementById("passwordOne");
  let randomSelection = "";
  let newPassword = "";

  for (let i = 0; i < 15; i++) {
    randomSelection = Math.floor(Math.random() * characters.length);
    newPassword += characters[randomSelection];
  }
  password.textContent = newPassword;
}

function copy() {
  const cb = navigator.clipboard;
  const paragraph = document.querySelector("passwordOne");
  cb.writeText(paragraph.innerText).then(() => alert("Text copied"));
}
